package com.sam.yh.crud.exception;

public class BindMobilePhoneException extends CrudException {

    public BindMobilePhoneException() {
        super();
    }

    public BindMobilePhoneException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public BindMobilePhoneException(String message, Throwable cause) {
        super(message, cause);
    }

    public BindMobilePhoneException(String message) {
        super(message);
    }

    public BindMobilePhoneException(Throwable cause) {
        super(cause);
    }

}